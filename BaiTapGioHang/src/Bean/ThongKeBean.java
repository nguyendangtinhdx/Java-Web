package Bean;

public class ThongKeBean {
    private String tenHang;
    private Long tongSoLuong;
    private Long trungBinhCong;
    
    public String getTenHang() {
        return tenHang;
    }
    public void setTenHang(String tenHang) {
        this.tenHang = tenHang;
    }
    public long getTongSoLuong() {
        return tongSoLuong;
    }
    public void setTongSoLuong(long tongSoLuong) {
        this.tongSoLuong = tongSoLuong;
    }
    public long getTrungBinhCong() {
        return trungBinhCong;
    }
    public void setTrungBinhCong(long trungBinhCong) {
        this.trungBinhCong = trungBinhCong;
    }
}
